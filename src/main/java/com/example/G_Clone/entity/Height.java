package com.example.G_Clone.entity;

public class Height {
    private int feet;
    private int inches;

    // Getters and Setters
    public int getFeet() {
        return feet;
    }

    public void setFeet(int feet) {
        this.feet = feet;
    }

    public int getInches() {
        return inches;
    }

    public void setInches(int inches) {
        this.inches = inches;
    }
}